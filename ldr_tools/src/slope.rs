pub fn is_slope_piece(name: &str) -> bool {
    let Some(name) = name.strip_suffix(".dat") else {
        return false;
    };
    // some parts have suffixes like a or b or p
    let name = name.trim_end_matches(|c: char| c.is_ascii_alphabetic());
    SLOPE_PIECES.contains(name)
}
